/*
O Spring é um conjunto de projetos em que cada um deles tem
a finalidade de solucionar um problema específico. dentre os mais populares são:

O Spring Framework: é responsável por tornar o nosso projeto apto para rodar na web.

Spring Data: Provê um modelo de acesso a banco de dados.

Spring Security: Cuida da parte da segurança da nossa aplicação.

Os projetos do ecossistema Spring são independentes, isso quer
dizer que podemos utilizá-los de forma isolada, como por exempl
o em um sistema web simples que só façá uso do Spring Framework
mas se porventura precisarmos de mais funcionalidades, podemos 
também combiná-los conforme a nossa necessidade.

O Spring é modular.

Outra característica do Spring é que seus projetos cuidam 
da infraestrutura da aplicação. E o que seria essa infraestrutura?
São partes da aplicação, importantes para o seu funcionamento, mas 
que não estão diretamente ligadas às suas regras de negócio.

Por exemplo, imagine que no front-end da sua aplicação existe um 
formulário. No back-end, certamente teremos uma classe pra esse 
formulário. Mas como converter o texto digitado pelo usuário em uma classe?

Sem o Spring MVC teríamos que escrever um código de conversão. 
Mas com o Spring MVC não precisamos nos preocupar com esse código 
de apoio, porque o framework se encarrega de entregar para o back-end 
uma classe devidamente populada sempre que um formulário for enviado.

Projetos Spring são voltados para infraestrutura.

O Spring foi feito para aplicações corporativas, que podem começar pequenas,
mas tendem a crescer à medida em que vão cobrindo mais áreas do negócio.
Na prática, isso quer dizer que é possível criar aplicações menores,
tipo um portal, blog, etc.

Sendo assim trabalhar com o Spring é um modo simples e confiável
de construir aplicações em Java.

Spring é ideal para grandes aplicações.

*/