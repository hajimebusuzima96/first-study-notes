/*
Iniciar e gerenciar um projeto em Angular pode não ser uma tarefa tão simples.
A existência de dezenas de bibliotecas, frameworks e ferramentas, que muitas 
vezes tentam resolver o mesmo problema, pode ser uma barreira aos primeiros
contatos com a tecnologia.

Ao observar esse problema, a equipe do Angular criou uma ferramenta de linha de 
comando chamada Angular CLI (Command Line Interface - Interface de Linha de Comando)
cujo objetivo principal é facilitar o gerenciamento de projetos escritos nesse framework.

Instalação do Angular CLI
O primeiro passo para utilizar o Angular CLI é realizar a sua instalação e, para tal,
é necessário ter o Node.JS instalado. Caso ainda não o tenha, acesse o site do Node.js 
e baixe a última versão destinada ao seu sistema operacional.

Com o Node.JS instalado, temos à disposição o comando npm, necessário para instalar 
o Angular CLI. Neste ponto, será preciso abrir o terminal do seu sistema operacional 
e digitar a instrução:

npm install -g @angular/cli
Essa instrução, normalmente, requer um bom tempo para que seja concluída, pois diversas
bibliotecas do Node.JS serão obtidas através da internet. Concluída a instalação, o 
Angular CLI está à nossa disposição através do comando ng, que é um programa para ser 
executado a partir da linha de comando e para o qual podemos passar diversos parâmetros. 
É dessa forma que criamos um novo projeto em Angular, criamos artefatos, realizamos o build
do projeto, entre outras opções.
*/