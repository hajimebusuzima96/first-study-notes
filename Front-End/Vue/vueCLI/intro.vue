Com o Node instalado o próximo passo é instalar o Vue CLI.


O comando para instalar o Vue CLI pode ser visto no Código 1.

npm install -g @vue/cli

Código 1. Comando usado para instalar o Vue CLI
Entenda o comando de instalação do Vue CLI: ao utilizar o comando
npm queremos que o gerenciador de pacotes do node execute alguma ação.
Nesse caso a ação é install, ou seja, instalar o pacote @vue/cli.
O parâmetro -g determina que este pacote será instalado de forma global
o deixando pronto para ser utilizado na próxima vez que precisarmos criar uma nova aplicação.

Nosso ambiente está preparado para o desenvolvimento de aplicações
em Vue.js utilizando o Vue CLI.

Já preparamos o ambiente e já temos tudo que é necessário para criar 
um aplicativo utilizando o Vue CLI, mas antes de criar um novo projeto 
Vue precisamos entender como utilizar o terminal.

O comando usado para abrir uma pasta pode ser visto no Código .

cd nome-da-pasta

Criando uma aplicação Vue.js utilizando o Vue CLI

Agora que já sabemos abrir pastas no terminal, podemos prosseguir 
e criar uma aplicação utilizando o Vue CLI.

O comando que é executado no terminal para criar um projeto utilizando
o Vue CLI pode ser visto no Código

vue create novo-projeto

Veja nos Códigos os comandos apresentados no flow acima.

cd novo-projeto

Comando usado para navegar para a pasta do projeto criado

npm run serve

Comando usado para executar a aplicação

